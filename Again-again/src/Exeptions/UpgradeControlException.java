package Exeptions;

/**
 *
 * @author Levi
 */
public class UpgradeControlException extends Exception {

    public UpgradeControlException() {
    }

    public UpgradeControlException(String message) {
        super(message);
    }

    public UpgradeControlException(String message, Throwable cause) {
        super(message, cause);
    }

    public UpgradeControlException(Throwable cause) {
        super(cause);
    }

    public UpgradeControlException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }

    
    
    
    
}
